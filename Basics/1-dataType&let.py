# python数据类型
# 整数 浮点数 字符串 布尔值 空值
# 布尔值可以用and、or和not运算
# 空值是Python里一个特殊的值，用None表示  None不能理解为0

print(True and True)
print(True or True)
print(True or False)
print(not True)

# 变量
# 动态语言
# 自动类型装配 无需初始化  干爆java
# 变量名 必须是大小写英文 数字 下换线_ 的组合 数字 不能 开头 
aa = 123
print(aa)
#type() 返回括号内 的数据类型
print(type(aa))

Answer = True
print(Answer)
print(type(Answer))

# 常量
# 不变的 变量
# 常量命名通常全部用大写

PI =3.14159265359

# ps: 除法计算
